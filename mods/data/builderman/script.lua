function onBeatHit()
	if curBeat == 64 then
		setProperty('defaultCamZoom',0.85)
	end

	if curBeat == 96 then
		setProperty('defaultCamZoom',0.4)
	end

	if curBeat == 208 then
		setProperty('defaultCamZoom',0.8)
	end

	if curBeat == 224 then
		setProperty('defaultCamZoom',0.6)
	end

	if curBeat == 256 then
		setProperty('defaultCamZoom',0.4)
	end

	if curBeat == 304 then
		setProperty('defaultCamZoom',0.75)
	end

	if curBeat == 320 then
		setProperty('defaultCamZoom',0.5)
	end

	if curBeat == 352 then
		setProperty('defaultCamZoom',0.4)
	end
end